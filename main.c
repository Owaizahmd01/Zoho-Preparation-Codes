
#include <stdio.h>

int main()
{
    float bs, dn, hr , grs ;

    printf("\n Enter the number: ");
    scanf("%f", &bs);
    dn = bs*0.4;
    hr = dn*0.2;
    grs = dn+hr+bs;

    printf("\n Gross Salary: %0.2f", grs);
    printf("\n dearness allowance: %0.2f", dn);
    printf("\n house rent: %0.2f",hr);
    printf("\n Basic Salary: %0.2f", bs);

    return 0;

}
